def buscar_coordenada_exacta(direccion):
    """
    Busca en el diccionario de coordenadas exactas
    Retorna (lat, lon) si encuentra, None si no
    """
    if not direccion:
        return None
        
    direccion_lower = direccion.lower().strip()
    
    # Búsqueda exacta primero
    if direccion_lower in COORDENADAS_EXACTAS:
        return COORDENADAS_EXACTAS[direccion_lower]
    
    # Búsqueda por coincidencia parcial
    for clave, coords in COORDENADAS_EXACTAS.items():
        if clave in direccion_lower or direccion_lower in clave:
            return coords
    
    return None
